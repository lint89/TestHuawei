/*********************************************************
 * 求最大连续bit数

 * 题目描述

 * 功能: 求一个byte数字对应的二进制数字中1的最大连续数，例如3的二进制为00000011，最大连续2个1
 *     
 * 输入: 一个byte型的数字
 *     
 * 输出: 无
 *      
 * 返回: 对应的二进制数字中1的最大连续数

 * 输入描述:
 * 输入一个byte数字


 * 输出描述:
 * 输出转成二进制之后连续1的个数

 * 输入例子:
 * 3

 * 输出例子:
 * 2
 *********************************************************/

#include <iostream>

using namespace std;


int main()
{
	int num;

	while (cin >> num)
	{
		int max = 0;
		int ret = 0;
		for (int i = 0; i < 8; ++i)
		{
			if (num & 1)
				++ret;
			else
				ret = 0;

			if (ret > max)
				max = ret;
				
			num = num >> 1;
		}
		cout << max << endl;

	}

}
